/**
 * @file StrategyEngine.hpp
 * @brief CRTP-based strategy engine framework.
 *
 * Implements the Curiously Recurring Template Pattern (CRTP) for
 * strategy dispatch, avoiding virtual function overhead on the hot path.
 *
 * Why CRTP over virtual functions:
 * - Eliminates vtable lookup (cache miss) on every signal
 * - Enables compiler inlining of strategy logic
 * - Zero-cost abstraction at runtime
 * - Compile-time polymorphism for strategy composition
 *
 * @author HFT NanoTick Team
 * @copyright MIT License
 */

